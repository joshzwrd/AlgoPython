"""
    Voici un super module qui fait quelque chose
    de grandiose etc.
"""
def maj(chaine):
    """
        ceci est une super fonction, qui renvoit 
        une chaine en majuscule.
    """
    return chaine.upper()

def ajouter(a, b):
    """
        ceci est une super fonction 2, qui renvoit 
        la somme de 2 nombres.
    """
    return a+b